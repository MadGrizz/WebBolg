     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required'
        ]);

        $myuser = new Myuser();
        $myuser->name=$request->get('name');
        $myuser->email=$request->get('email');
        $myuser->password=$request->get('password');
        $myuser->save();

        return view('welcome');
    }

